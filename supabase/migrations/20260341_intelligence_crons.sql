-- Activate dormant intelligence functions + new grader/calibration crons

-- Anomaly Detector: daily 9:30am UTC (after convergence engine at 8am)
SELECT cron.schedule(
  'hunt-anomaly-detector',
  '30 9 * * *',
  $cron$
  SELECT net.http_post(
    url := (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'project_url') || '/functions/v1/hunt-anomaly-detector',
    headers := jsonb_build_object(
      'Content-Type', 'application/json',
      'Authorization', 'Bearer ' || (SELECT decrypted_secret FROM vault.decrypted_secrets WHERE name = 'service_role_key')
    ),
    body := '{}'::jsonb
  );
  $cron$
);

